// Get dates for a specific month
export const getDatesForMonth = (year, month) => {
  return examDates.filter(exam => {
    const examDate = new Date(exam.date);
    return examDate.getFullYear() === year && examDate.getMonth() === month;
  });
};

// Get dates for a specific day
export const getDatesForDay = (dateString) => {
  return examDates.filter(exam => exam.date === dateString);
};

// Get upcoming exam dates
export const getUpcomingDates = () => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  return examDates.filter(exam => {
    const examDate = new Date(exam.date);
    examDate.setHours(0, 0, 0, 0);
    return examDate >= today;
  }).sort((a, b) => new Date(a.date) - new Date(b.date));
};
